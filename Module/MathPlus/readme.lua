    MATHSPLUS
    -------------

    An open-source library created by iSophes expanding on Luau's math functionality adding
    other library's functions into luau whilst adding all of the default functions into
    this module so all your math functions are in one module.

    Designed with scalability and ease of use in mind. I'll try and keep a clean codebase
    with the module. Open source because why shouldn't it be?

